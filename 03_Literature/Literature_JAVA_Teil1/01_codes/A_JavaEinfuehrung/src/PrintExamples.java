public class PrintExamples {
    public static void main(String[] args) {

        // Vewenden von println für die Ausgabe mit einer Zeilenumbruch am Ende

        System.out.println("Hallo, dies ist ein Beispiel!");
        System.out.println("Jede Println-Ausgabe wird in einer neuen Linie angezeigt!");


        // Vewenden von print für die Ausgabe ohne einer Zeilenumbruch am Ende

        System.out.print("Hallo, dies ist ein Beispiel!");
        System.out.print("Jede Print-Ausgabe wird in der gleiche Linie angezeigt!");

        // Kombination von print und println
        System.out.println("Hallo, dies ist ein Beispiel!");
        System.out.print("hier wird eine neue Zeile");



    }
}
